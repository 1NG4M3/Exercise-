package GeekBrainsStage1.lesson1.lesson6;

public class Cat extends Animal {
    private int z = 200;
    private String s = "Кот не умеет плавать";
    protected static int number;

    public Cat() {
        super.l = l;
        this.z = z;
        this.s = s;
        number++;
    }

    static int run(int z) {
        return z;
    }

    static String swim(String s) {
        return s;
    }
}
